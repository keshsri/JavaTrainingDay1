public class Operators {
    public static void main(String[] args){
        int a=5,b=5;
        double f=5.5;
        double sum;
        sum = a + b + f;
        System.out.println(sum);

        boolean x = 10>=20;
        System.out.println(x);

        boolean t = true;
        boolean q = false;
        System.out.println(t && q);
        System.out.println(t || q);
        System.out.println(t == q);

        int i = 10;
        int j = 20;
        System.out.println(i > j);
        System.out.println(i < j);
        System.out.println(i >= j);
        System.out.println(i <= j);
        System.out.println(i != j);

    }
}
